    a=x/1000;
    b=(x-a*1000)/100;
    c=(x-a*1000-b*100)/10;
    d=x-a*1000-b*100-c*10;
    a=(a+5)%10+65;
    b=(b+5)%10+65;
    c=(c+5)%10+65;
